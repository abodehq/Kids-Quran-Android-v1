package com.isslam.kidsquran.model;

public class Gifts {

	private int cat_id;
	private int item_id;
	private int status;
	private int count;
	// -----------
	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	// -----------
	public int getCatId() {
		return cat_id;
	}

	public void setCatId(int cat_id) {
		this.cat_id = cat_id;
	}
	// -----------
	public int getItemId() {
		return item_id;
	}

	public void setItemId(int item_id) {
		this.item_id = item_id;
	}
	// -----------
	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}


}